# https://quera.org/problemset/3403

num_1 = float(input())
num_2 = float(input())
num_3 = float(input())
num_4 = float(input())

sum = num_1 + num_2 + num_3 + num_4
avrage = sum / 4
product = num_1 * num_2 * num_3 * num_4
min = min(num_1, num_2, num_3, num_4)
max = max(num_1, num_2, num_3, num_4)

print(f"Sum : {sum:.6f}")
print(f"Average : {avrage:.6f}")
print(f"Product : {product:.6f}")
print(f"MAX : {max:.6f}")
print(f"MIN : {min:.6f}")


